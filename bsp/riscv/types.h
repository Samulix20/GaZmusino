#ifndef TYPES_H
#define TYPES_H

#include <stddef.h>
#include <stdint.h>

// Standard type sizes
typedef int8_t int8;
typedef uint8_t uint8;

typedef int16_t int16;
typedef uint16_t uint16;

typedef int32_t int32;
typedef uint32_t uint32;

typedef int64_t int64;
typedef uint64_t uint64;

// Rust standard type sizes
typedef int8 i8;
typedef uint8 u8;

typedef int16 i16;
typedef uint16 u16;

typedef int32 i32;
typedef uint32 u32;

typedef int64 i64;
typedef uint64 u64;

#endif